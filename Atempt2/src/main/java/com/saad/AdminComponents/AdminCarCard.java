    public static byte[] getImageDataFromDatabase(int carRegistrationNumber) {
    byte[] imageData = null;
    Connection connection = DBConnection.getConnection();
    String query = "SELECT picture FROM car WHERE registration_number = ?";

    try (
         PreparedStatement statement = connection.prepareStatement(query)) {
        statement.setInt(1, carRegistrationNumber);
        ResultSet resultSet = statement.executeQuery();
        if (resultSet.next()) {
            imageData = resultSet.getBytes("picture");
        }
    } catch (SQLException e) {
        e.printStackTrace();
    }

    return imageData;
}
    
    
