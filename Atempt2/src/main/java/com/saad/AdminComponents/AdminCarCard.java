    private void EditCarButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EditCarButtonMouseClicked

        try {
            Car c =null ;
            
            c = Car.findCar(Integer.parseInt(this.CarId.getText()));
            
            // Get the parent frame
            AdminFrame adminFrame = (AdminFrame) SwingUtilities.getWindowAncestor(this);

            // Get the CardLayout from the main JPanel
            CardLayout cardLayout = (CardLayout) adminFrame.getAdminCardPanel().getLayout();

            // Get the panels by their names
            JPanel addCarFrameSP1 = adminFrame.getaddCarFrameSP1();
            JPanel adminCarFrameSP1 = adminFrame.getadminCarFrameSP1();
            JPanel adminClientTable1 = adminFrame.getadminClientTable1();
            JPanel adminReservationTable1 = adminFrame.getadminReservationTable1();
            JPanel editCarFrame1 = adminFrame.geteditCarFrame1(c);
            

            // Set the visibility of the panels based on their names
            cardLayout.show(adminFrame.getAdminCardPanel(), "addCarFrameSP1");
            addCarFrameSP1.setVisible(false);
            adminClientTable1.setVisible(false);
            adminReservationTable1.setVisible(false);
            adminCarFrameSP1.setVisible(false);
            editCarFrame1.setVisible(true);
            
        } catch (SQLException ex) {
            Logger.getLogger(AdminCarCard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_EditCarButtonMouseClicked

